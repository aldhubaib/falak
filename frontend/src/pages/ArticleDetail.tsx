const NODE_STATE_STYLES: Record<NodeState, { border: string; bg: string; text: string; ring: string }> = {
  completed: { border: "border-success/40", bg: "bg-card", text: "text-success", ring: "ring-success/20" },
  failed:    { border: "border-destructive/40", bg: "bg-card", text: "text-destructive", ring: "ring-destructive/20" },
  review:    { border: "border-orange/40", bg: "bg-card", text: "text-orange", ring: "ring-orange/20" },
  active:    { border: "border-primary/40", bg: "bg-card", text: "text-primary", ring: "ring-primary/20" },
  waiting:   { border: "border-border/50", bg: "bg-card", text: "text-muted-foreground", ring: "" },
  skipped:   { border: "border-border/50", bg: "bg-card", text: "text-muted-foreground", ring: "ring-border/20" },
};

function formatDuration(ms: number): string {
  const s = Math.floor(ms / 1000);
  if (s < 60) return `${s}s`;
  if (s < 3600) return `${Math.floor(s / 60)}m ${s % 60}s`;
  const h = Math.floor(s / 3600);
  if (h < 24) return `${h}h ${Math.floor((s % 3600) / 60)}m`;
  return `${Math.floor(h / 24)}d ${h % 24}h`;
}

function getStageDurations(log: LogEntry[]): Record<string, number> {
  const durations: Record<string, number> = {};
  const stageFirstAt: Record<string, number> = {};

  for (const entry of log) {
    const stage = entry.stage;
    if (!stage || !entry.at) continue;
    const ts = new Date(entry.at).getTime();
    if (!ts || isNaN(ts)) continue;

    if (!(stage in stageFirstAt) || ts < stageFirstAt[stage]) {
      stageFirstAt[stage] = ts;
    }

    if (entry.step === "verdict" && stageFirstAt[stage]) {
      durations[stage] = ts - stageFirstAt[stage];
    }
  }

  return durations;
}

function LiveTimer({ since }: { since: string }) {
  const [elapsed, setElapsed] = useState("");
  useEffect(() => {
    const start = new Date(since).getTime();
    const tick = () => {
      const s = Math.floor((Date.now() - start) / 1000);
      if (s < 60) setElapsed(`${s}s`);
      else if (s < 3600) setElapsed(`${Math.floor(s / 60)}m ${s % 60}s`);
      else setElapsed(`${Math.floor(s / 3600)}h ${Math.floor((s % 3600) / 60)}m`);
    };
    tick();
    const id = setInterval(tick, 1000);
    return () => clearInterval(id);
  }, [since]);
  return <span className="text-[10px] font-mono text-primary tabular-nums">{elapsed}</span>;
}

